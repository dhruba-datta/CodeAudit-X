def find_max(numbers):
    # return the largest number in the list
    return max(numbers)

# Test the function
print(find_max([1, 2, 3, 4, 5]))  # Output: 5
print(find_max([-1, -2, -3, -4, -5]))  # Output: -1
print(find_max([10, 20, 30, 40, 50]))  # Output: 5