def reverse_string(s):
    """
    Given a string, return the string in reverse order.

    >>> reverse_string('hello')
    'olleh'
    >>> reverse_string('Hi')
    'Hi'
    >>> reverse_string('Python')
    'Python'
    >>> reverse_string('Hi')
    'Hi'
    >>> reverse_string('Hi')
    'Hi'
    """
    return s[::-1]
